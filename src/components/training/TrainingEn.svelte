    <h1>Training dogs</h1>
    <p> Dogs don't understand difficult sentences. “Benny, I want you to be a good boy and
        sit for me now” will likely earn you a blank stare. Keep verbal cues short and uncomplicated. Additionally, dogs
        learn first from our body language, so it is important that everyone in the family use the same body language.
    </p>
    <p>Everyone in the family should use the <u>same</u> cues, otherwise your dog may get confused. It might help to
        post a list of cues where everyone can become familiar with them. </p>
    <b>Rule of thumb:</b><br>
    <li>Dogs live in the moment, praise needs to come immediately after the have done something good. If they have done
        something that you don't like, shake it off and don't dwell on it. If you stay in a bad mood the dog will notice
        but will <u>not</u> understand why.</li>
    <li>Always be kind to the dog, don't scold him or yell at him - this is bad for the relationship between you</li>
    <li>If something doesn't work the first time you ask him to do something, there is something in the environment or
        some other reason why it isn't working and it won't work if you ask again, you'll only make it more likely that
        he
        will ignore you.</li>
    <h2>Generalizing and dogs</h2>
    <p>Dogs do not generalize as well as us humans do, so it can make it very difficult for us to understand what they
        are going through when we ask them to do a behavior they already know in a scenario that they are unfamiliar
        with. </p>
    <p>An example of how dogs do not generalize well is: You may think to yourself, “my dog knows sit”. But if you stand
        with your head buried in the corner of the room and ask your dog to sit, he most likely won’t. Also, if your dog
        only fetches a ball and you throw your car keys for your dog and say “Fetch”, most likely your dog will look
        confused and not pick them up. </p>
    <p>Generalizing a dog’s behavior is the process of reproducing the desired behavior in various environments,
        settings, and conditions different from the initially trained behavior in the original environment.</p>
    <h2>How to train generalization</h2>
    <p>Once you have gotten a behavior on cue, it’s time to move on to having your dog perform the behavior on different
        surfaces, in different locations, with different distractions, and in different positions in relation to
        yourself. More about this under "obedience".</p>
    <p>Addditionally to the environment etc being different, your posture (standing, sitting..) as well as your
        position to the dog (right in front, to the side etc) matters. For example you facing the dog asking him to back
        up in the kitchen might work. But if you are facing away from the dog and you ask him to backup, and you haven't
        taught him it - it will NOT work.</p>
    <p>Dog training should be fun for all parts, if your training is going poor - do something easy that the dog knows
        well to make sure to <b>end on a good dog training note</b> when your dog does a wonderful iteration of whatever
        behavior you request. </p>
    <h2>Mental stimulation and training for dogs</h2>

    <p>Both physical and mental stimulation is equally important for dogs to keep your dog healthy and in good
        condition. Working dogs such as retrievers and collies are extra smart dogs and need to be stimulated every day.
        Lack of mental stimulation can lead to behavioural problems and stress.
    </p>

    Here is a list of examples of activites for mental stimulation:
    <ul>
        <li>Let Your Dog Sniff & Explore on Walks</li>
        <li>Let your dog play with a Puzzle Toy (wooden puzzle or black Kong)</li>
        <li>Obedience training</li>
        <li>Nose Work</li>
        <li>Teach Your Dog the Names of Their Toys</li>
        <li>Make an Obstacle Course For Your Dog & body control</li>
    </ul>
    <h2>Obedience training</h2>
    <p>Golden retrievers love to work and training obedience is a great way to excersise that need. What is important to
        remember is to always progress and make the training little by little more complex for the dog. If you
        <b>don't</b> do it, it is <u>not</u> stimulating for the dog. Something to
        keep in mind is that dogs <b><u>can not!</u> </b>generalize. In practise this means that just because they can
        "sit" on command in the kitchen, does <u>not</u> mean that they can do it outside where it is more distracting.
        When you practise obedience you therefore need to take it very slow and make sure that the dog succeeds. An
        example of how you can progress obedience is with the command "stay". The goal
        here is for the dog to remain in one place. Example: <br>
    <ol>
        <li>Teach the dog to remain in a very easy environment, such as the kitchen. When you have done it
            succesfully
            three times, you can make it more difficult, for example: dog stays in the kitchen and you walk out
            to the
            living room. If the dog remains, go back and praise. If the dog fails, ignore and try again but just
            walk to
            the hallway. You always want to ensure that the dog is successful to keep it fun!</li>
        <br>
        <li>When you feel like you have conquered the "stay" in the kitchen, you can practise it in the
            livingroom and walk into the bedroom, kitchen etc and slowly progress. If the dog fails, just ignore the
            failure and try again but make it easier.</li>
        <br>
        <li>When the dog can "stay" successfully in every room you can now try to make it more difficult. Examples of
            making it more difficult is to advance the time for the dog to remain until you go and praise him. Another
            example is that you go into another room and make some distracting noises (first very very easy and then you
            can progress to a bouncing ball for example).</li>
        <br>
        <li>There are plenty of ways to advance the "stay" command, such as throwing toys as a distraction to for
            example, asking the dog to remain while you jog away from the dog.</li>
        <br>
        <li>Next level is of course to try this outside. Please note and remember that training is <u><b>always</b></u>
            more difficult outside since you compete with scent, sticks, other people, dogs etc. Therefore you need to
            take the training much slower outside than you would inside. You start very easy with just a few meters and
            then advance from there. </li>
    </ol>
    Another example of obedience trainin is learning a new trick, the example Georgi started teaching Benny is to spin
    around his body.
    I will explain the process that Georgi started below:
    <ol>
        <li>Guiding with a treat to lead the dog to where you want him, reward with treats.</li>
        <li>Progress to lead the dog without treat (only hand movement), reward with treat and "BRAVO" once the dog
            "understands"</li>
        <li>Do step nr 2, several times to ensure that the dog gets it.</li>
        <li>You can now add a cue command, you say the cue then do the movement and praise the dog when he is in "goal"
            and give treat.</li>
        <li>Do nr 3 several times and then try to just say the command word and see if the dog understands, if not do
            the hand sign as well.</li>
        <li>Once the dog knows the command without hand sign you can do the excersise in another room.</li>
        <li>Once the dog can do it in the kitchen, living room and hallway, you can try two circles around you and
            praise when completed.</li>
        <li>OBS!<u> Always</u> rememeber to take the training 1-2 steps back if the dog fails, the training should be
            fun and encouraging and not boring and too difficult.</li>

    </ol>

    <p>Another way to make obedience training more difficult is to do combinations of commands, for example:
        "sit/sedni", "dowo" and "nazad", after your commands are done correctly by the dog - reward. If the dog fails,
        make sure to make it easier next time. Remember: mix up the commands, and also the order you say it so you
        aren't predictable. </p>
    </p>

