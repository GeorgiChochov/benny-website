    <h3>Nosework</h3>

    <p> Nosework is a great activity for dogs who like to sniff a lot. Nosework done right, is quite challenging and
        should be done for short intervalls, approx 10 minutes a time. It can be done 3-4 times a week. In the example
        picture, you see an example of container search.</p>
    Below is a summary of the steps:
    <ol>
        <li>Put some ointment on a small Stick-on floor protectors (залепващи протектори за под)</li>
        <li>Ask the dog to remain when you hide the little sticker</li>
        <li>Go get the dog and say the command "search"</li>
        <li>When the dog has found where you have hidden it, and marked (Benny looks at you and sometimes sits down) -
            praise, walk up to him and give a treat right where the hidden scent is.</li>
        <li>Go back to 1 and start over</li>
        <li>To advance the nosework game, you can put the sticker further up or down (closer to the floor). The next
            level is to try in another room.</li>
    </ol>
