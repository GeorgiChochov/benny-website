    <h2>Obstacle course and body control</h2>
    <p>Building a simple obstacle course for your dog is great for their body control and stimulates both the body and
        mind. The obstacles should be possible to tear down easily if the dog fails to ensure that the dog doesn't get
        hurt. </p>
    <p>There are plenty of ways you can make obstacles, only your imagination sets the limit - just make sure you have
        enough room for what you want to pursue.</p>
    <b>Ideas:</b>
    <li>Put up something and show the dog to crawl under and praise</li>
    <li>Put up and obstacle that the dog needs to jumpo over and praise</li>
    <li>Put up sticks for slalom (see example picture), or teach the dog to go between your legs</li>
    <p>Backing up (Nazad) is a really a great way for the dog to learn body control. Another one is balance excersises
        (Google it).</p>
