    <h1>Winter for dogs</h1>

    <p>Winter is usually a little better than summer for dogs due to the fact that many dogs handle cool weather better
        than hot weather. Many dogs love snow but the snow can hurt their paws, it can be sharp and give them wounds.
        During the walk, you should look out if your dog is lifting its paw. If this happens, they are probably to cold.
        To help your dog,
        you can lift up your dogs paw in your hand and warm it a little. When you come back from a winter walk,
        especially if it has been snowing or been snow on the ground, you need to clean the paws properly. 4-7 times a
        week, you should put cream under the paws so they don't dry out.</p>
    <br>
    Here are some advice on what to do:
    <ul>
        <li>Make sure to use reflexes on yourself when it is dark out</li>
        <li>Consider buying a reflex for benny, for example a bandana or a vest</li>
        <li>Wash the paws after every walk, if it is snow outside make sure to remove the snow lumps</li>
        <li>Proactively - cut the fur between his toes</li>
        <li>Use ointment under the pads to ensure that they don't dry out</li>
        <li>In case of wound/wounds on the pad, the dog can wear socks</li>
    </ul>

