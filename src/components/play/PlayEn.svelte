    <h1>Dog play</h1>
    <img class="imageRight" src="../../images/play.gif">

    <p>Playing with your dog is fun and strenghtens the relationship between you both.</p>
    <h2>Tug of war</h2>
    <p>This is a fun and stimulating game for the dog and is a healthy display of their predatory nature.
        The game should be fun and safe for both parts, to ensure this bear the following things in mind:<br>
        - Remember the relase command - "drop it"<br>
        - The word for grab on the toy is "tug"<br>
        - Keep the toy low, so he doesn't jump up<br>
    <p>Sometimes you win (the toy is yours)and sometimes you should let him win (the dog gets to keep the toy).</p>
    </p>
    <h2>Fetch</h2>
    <p>Fetch is a fun game to play for the dog which makes him chase after the ball/toy and then bring it back to you.
        The word for fetch is "apport". When the dog walks or runs to the toy and grabs it you can say "bravo" or start
        backing up. When the dog brings it back to you you can pet him and say bravo, and then ask him to "drop it" and
        then toss it again. To strenghten the dog's skills in fetching, it is recommended to back up while the dog is on
        its way back to you.</p>
