    <h1>Food and water for dogs</h1>
    <p>A golden retriever that is moderatly activie, weighing 32 kg should drink between 1-2 liters of water depending
        on the season. It is best to offer fresh water, instead of refilling the water bowl you should pour out the
        water and fill it up with new fresh water.</p>
    <p> Dogs should mainly eat their dog food and secondly dog
        treats but can occassionaly have some safe human
        ingredients when supervised and in very small quantities. Benny can additionally have one chew-bone a day.
    </p>
    <img class="treat" src="../../images/britcare_krill.jpg">
    <br>
    <br>
    <h2>Good dog treats</h2>

    <p>When you buy treats for a dog you want to make sure that they are somewhat healthy. You want to look for "high
        meat/fish content" and "grain free". Recommended brands are Britcare & Carnilove, Carnilove is slightly cheaper
        per kilo. Both can be found at Maxipet. Another good brand is tundrasnacks, but that is also slightly more
        expensive.
    </p>

    <h2>Human food for treats</h2>
    <p>Dogs can occassionaly have some safe human ingredients when supervised and in very small quantities. Below you
        can find some of those that are safe as well as Benny likes. Below that you can find a list of toxic items as
        well.
    </p>
    <ul>
        <li>Chicken - boiled and <b>unsalted</b></li>
        <li>Carrots - best raw</li>
        <li>Cucumber</li>
        <li>Broccoli, cauliflower - just a little, or he can get gassy</li>
        <li>Yoghurt - preferably lactose free - alos just a little</li>
        <li>Soft cheese/cheese (kashkaval) - small amounts since it contains lactose. </li>
        <li>Peanut butter (without sugar) - high in kcal so only about 1 teaspoon, for example on top of a kong</li>
    </ul>
