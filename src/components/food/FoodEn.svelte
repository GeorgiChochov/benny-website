    <h2 class="h2">!Toxic for dogs</h2>
    <p>Dogs can eat several fruits but make sure to remove the seeds on for example a water melon and apples.<br>
        <b>If any of the items on the list below is consumed, please consult a vetrinerian!</b>
    </p>
</body>
<ol>
    <li>SALT</li>
    <p>Salt is poisonous to dogs and can result in signs of vomiting, diarrhea, decreased appetite, lethargy,
        incoordination, excessive thirst or urination. In severe cases, seizures, coma, and even death are possible.
    </p>
    <li>ONIONS AND CHIVES</li>
    <p>The onion family, whether dry, raw or cooked, is particularly toxic to dogs and can cause
        gastrointestinal
        irritation and red blood cell damage. Signs of illness are not always immediate and can occur up to a
        few
        days
        later.</p>

    <li> CHOCOLATE & Cocoa</li>
    <p>Chocolate and cocoa is another poisonous food for dogs. Chocolate and cocoa contains a stimulant called
        theobromine
        (dark chocolate has the highest content of this) which is toxic to dogs and can cause kidney failure.
    </p>

    <li>MACADAMIA NUTS</li>
    <p>Macadamia nuts contain a toxin that can affect your dog’s muscles and nervous system resulting in
        weakness,
        swollen
        limbs and panting.</p>

    <li>CORN ON THE COB</li>
    <p>Corn on the cob could potentially be fatal if eaten by your dog. Although the corn is digested by dogs,
        the
        cob
        cancause a blockage in your dog’s intestine.</p>

    <li>AVOCADO</li>
    <p>Avocados are another poisonous food for dogs. Avocado plants contain a substance called Persin which is
        in
        its
        leaves, fruit and seed and can cause vomiting and diarrhoea in dogs.
    </p>

    <li>ARTIFICIAL SWEETENER (XYLITOL)</li>
    <p>Diet and sugar-free products such as chewing-gum and soda usually consists of Xylitol. If a dog eats a
        product
        with
        Xylitol, they can go into hypoglycaemia which is linked to liver failure and blood clotting disorders.</p>

    <li>ALCOHOL</li>
    <p> has a huge impact on dogs even in small doses. The drink not only causes intoxication as it does in
        humans,
        but it can lead to sickness, diarrhoea and even central nervous system damage.</p>

    <li>COOKED BONES</li>
    <p> Bones should be avoided at all cost. These can easily splinter
        and in large quantities cause constipation or at worst, a perforation of the gut which can be fatal.</p>

    <li>GRAPES AND RAISINS
        <p>Grapes and raisins may cause severe liver damage and kidney failure.</p>
</ol>
