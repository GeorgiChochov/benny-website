    <h2>Body language</h2>
    <p>Learning to study a dog's body langauge is important because it tells you a lot about how they are doing. It can
        be difficult at first but you can normally see several things happening within a short period of time, for
        example yawning, lip licking and scratching.
        The first row in the picture to the right (green) shows a happy dog. The second row (blue), shows a worried dog.
        Additonally to this is also when the dog scratch - although sometimes they do this because something is actually
        scratching. On the third row (red) you can see a very stressed dog.
    </p>
    <p>Once you learn your dog's body language, you can help and guide them in different situations. You should never
        rush a dog showing signs of anxiety and stress. If you work through a tough situation for the dog together, this
        can strenghten your relationship and vice versa. </p>

    <h2>Adolecense</h2>
    <p>Large dogs mature slower than small dogs. For dogs at the same size as Benny, this means that they are teenagers
        for a longer time - usually between approx 6 months of age until about 2-3 years of age. The most difficult time
        is usually between 8-18 months. Teenage dogs is quite similar to teenage humans with hormone levels going up and
        down. This is a difficult time for the dog and sometimes it can feel like the dog has cotton in its ears. What
        is the most important here is to be patient, stick to positive reinforcement and be consistent with the dog. If
        your rule is that the dog can't jump up on the bed, you should <u>never</u> allow this. </p>

    <b>Typical behaviour during adolesence:</b>
    <ul>
        <li>Very interested in scents </li>
        <li>Marks with urin more often</li>
        <li>Tries boundries, what happens if I ignore my owner/s?</li>
        <li>Easily distracted </li>
    </ul>
    <p>Just as with human teenagers, this phase might feel long but it will pass as long as you stay consistent and
        continue the training.</p>
