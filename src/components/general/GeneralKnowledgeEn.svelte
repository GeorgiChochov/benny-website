    <h1>Helpful facts about dogs</h1>
    <p>According to several behavioral measures, dogs’ mental abilities are close to a human child age 2 to
        2.5 years with an IQ of about a 100. The dog's breed determines some of its ability to learn and to work with
        its human. Golden retrievers are known for their impulse to please their humans and love to work. Their limited
        IQ however means that they aren't as smart as humans and do not understand commands and cues unless you train
        them properly. Dogs do not generalize as well as us humans do, in practise this means that just because they
        know a behaviour inside the house, doesn't mean that they know how to do the same thing in another environment
        (unless we train them to). More about this under "training".</p>
    <h2>Positive reinforcement</h2>
    <p>Dogs repeat behaviors that are rewarding for them. For example if you know that your dog always chew sticks at
        the same spot on the grass, you want to <b>foresee</b> the behaviour and prevent it - have them do something
        else instead. The more they learn to do something and get rewarded for it (chew sticks, barks etc) the more
        likely they are to do it. This also goes for good behaviour, if they learn that they get a treat everytime they
        sit - they are more likely to perform the behaviour. </p>
    <h3>Timing</h3>
    <p>Correct timing is essential when using positive reinforcement. The cue and reward must occur immediately (within
        seconds) of the desired behavior, or your pet may not associate it with the proper action. For example, if you
        have your dog sit but reward them after they’ve stood back up, they’ll think they’re being rewarded for
        standing.
    </p>
