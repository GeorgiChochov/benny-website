    <h1>Полезни факти за кучетата</h1>
    <p>Според няколко поведенчески мерки, умствените способности на кучетата са близки до човешкото дете на възраст от 2
        до
        2,5 години с IQ около 100. Породата на кучето определя част от способността му да учи и да работи с
        човешко е. Голдън ретривърите са известни със своя импулс да угодят на хората си и обичат да работят. Техните
        ограничени
        IQ обаче означава, че те не са толкова умни като хората и не разбират команди и знаци, освен ако не тренирате
        ги правилно. Кучетата не обобщават така добре, както ние, хората, на практика това означава, че само защото те
        знаят поведение в къщата, не означава, че знаят как да правят същото в друга среда
        (освен ако не ги обучим за това). Повече за това в "обучение".</p>
    <h2>Положително подсилване</h2>
    <p>Кучетата повтарят поведения, които са възнаграждаващи за тях. Например, ако знаете, че вашето куче винаги дъвче
        клечки
        на същото място на тревата, искате да <b>предвидите</b> поведението и да го предотвратите - накарайте ги да
        направят нещо
        вместо това. Колкото повече се научават да правят нещо и получават възнаграждение за това (дъвчат пръчки, кори и
        т.н.), толкова повече
        вероятно ще го направят. Това важи и за доброто поведение, ако научат, че всеки път получават лакомство
        седнете - те са по-склонни да изпълнят поведението. </p>
    <h3>Време</h3>
    <p>Правилното време е от съществено значение, когато използвате положително подсилване. Репликата и наградата трябва
        да се появят незабавно (в рамките на
        секунди) от желаното поведение или вашият домашен любимец може да не го свърже с правилното действие. Например,
        ако вие
        накарайте кучето си да седне, но го наградете, след като се изправи, те ще си помислят, че са възнаградени за
        стоящ.
    </p>
