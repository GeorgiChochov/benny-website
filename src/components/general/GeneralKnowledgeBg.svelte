    <h2>Език на тялото</h2>
    <p>Да се ​​научите да изучавате езика на тялото на кучето е важно, защото ви казва много за това как се справят. То
        може
        е трудно в началото, но обикновено можете да видите как се случват няколко неща за кратък период от време, т.е
        например прозяване, облизване на устни и почесване.
        Първият ред на снимката вдясно (зелен) показва щастливо куче. Вторият ред (син) показва притеснено куче.
        В допълнение към това е и когато кучето се почеше - въпреки че понякога правят това, защото нещо наистина е
        надраскване. На третия ред (червен) можете да видите много стресирано куче.
    </p>
    <p>След като научите езика на тялото на вашето куче, можете да му помогнете и да го напътствате в различни ситуации.
        Никога не трябва
        бързайте с куче, показващо признаци на тревожност и стрес. Ако заедно преодолеете трудна ситуация за кучето,
        това
        може да укрепи връзката ви и обратно. </p>

    <h2>Юношество</h2>
    <p>Големите кучета узряват по-бавно от малките. За кучета със същия размер като Бени това означава, че са
        тийнейджъри
        за по-дълго време - обикновено между около 6-месечна възраст до около 2-3-годишна възраст. Най-трудният момент
        обикновено е между 8-18 месеца. Тийнейджърските кучета са доста подобни на тийнейджърите с нива на хормони,
        които се повишават и
        надолу. Това е труден момент за кучето и понякога може да почувства, че кучето има памук в ушите си. Какво
        най-важното тук е да бъдете търпеливи, да се придържате към положителното подсилване и да бъдете последователни
        с кучето. Ако
        вашето правило е, че кучето не може да скача на леглото, вие не трябва <u>никога</u> да допускате това. </p>

    <b>Типично поведение по време на юношеството:</b>
    <ul>
        <li>Много се интересувам от аромати </li>
        <li>По-често белези с урина</li>
        <li>Опитва граници, какво ще стане, ако игнорирам/ите си собственик/и?</li>
        <li>Лесно се разсейва </li>
    </ul>
    <p>Точно както при човешките тийнейджъри, тази фаза може да изглежда дълга, но ще премине, стига да останете
        последователни и
        продължете обучението.</p>
